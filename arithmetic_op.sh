#!/bin/sh
#Adding
VAR1=`expr 2 + 2`
echo "Adding value 2 and 2. Result is : " $VAR1


#Subtracting
VAR1=`expr 2 - 2`
echo "Subtracting value 2 and 2. Result is : " $VAR1



#Division
VAR1=`expr 12 / 12`
echo "Division value 12 and 12. Result is : " $VAR1




#Multiplication
VAR1=`expr 2 \* 6`
echo "Multiplication value 2 and 6. Result is : " $VAR1




#Modulus
VAR1=`expr 5 % 2`
echo "Remainder of  value 5 and 2. Result is : " $VAR1



